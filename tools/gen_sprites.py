SPRITES = {}


def sprite(name, art, mirror=False):
    rows = [r for r in art.strip('\n').split('\n')]
    w = len(rows[0])
    for r in rows:
        assert len(r) == w, f"{name}: ragged row {r!r}"
    if mirror:
        rows = [r + r[-2::-1] for r in rows]
    SPRITES[name] = rows


