"""Generate src/gfx/sprites.{h,c} from the ASCII pixel art below.

Each glyph character maps to a palette entry; '.' is transparent.
Rows must be equal length. mirror=True mirrors the art horizontally
around its last column (draw the left half plus the centre column).

Run from the repository root:  python3 tools/gen_sprites.py
"""

