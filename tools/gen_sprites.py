def rgb565(rgb):
    r, g, b = rgb
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3)


def main():
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    chars = list(PALETTE.keys())  # '.' first -> index 0 transparent

    h = ["// Generated by tools/gen_sprites.py - edit the art there, not here.",
         "#ifndef NOVA_SPRITES_H", "#define NOVA_SPRITES_H", "",
         '#include "gfx/gfx.h"', "",
         f"extern const uint16_t sprite_palette[{len(chars)}];", ""]
    c = ["// Generated by tools/gen_sprites.py - edit the art there, not here.",
         '#include "gfx/sprites.h"', "",
         f"const uint16_t sprite_palette[{len(chars)}] = {{"]
    pal_entries = []
    for ch in chars:
        v = rgb565(PALETTE[ch]) if PALETTE[ch] else 0
        pal_entries.append(f"0x{v:04X}")
    c.append("    " + ", ".join(pal_entries))
    c.append("};\n")

    for name, rows in SPRITES.items():
        w, hgt = len(rows[0]), len(rows)
        c.append(f"static const uint8_t px_{name}[{w * hgt}] = {{")
        for row in rows:
            vals = [str(chars.index(ch)) for ch in row]
            c.append("    " + ",".join(vals) + ",")
        c.append("};")
        c.append(f"const sprite_t spr_{name} = {{ {w}, {hgt}, px_{name} }};\n")
        h.append(f"extern const sprite_t spr_{name};")

    h += ["", "#endif", ""]

    with open(os.path.join(root, "src/gfx/sprites.h"), "w") as f:
        f.write("\n".join(h))
    with open(os.path.join(root, "src/gfx/sprites.c"), "w") as f:
        f.write("\n".join(c) + "\n")
    print(f"wrote {len(SPRITES)} sprites")


if __name__ == "__main__":
    main()
